    private void listarItensVendidos() {
        ProdutosDAO dao = new ProdutosDAO();
        ConectaDAO conecta = new ConectaDAO();
        
        try {
            boolean status = conecta.connectDB();

            if (status == false) {
                JOptionPane.showMessageDialog(null, "Erro de conex√£o");
            } else {
                ArrayList<ProdutosDTO> listagem = dao.listarProdutosVendidos();

                DefaultTableModel model = (DefaultTableModel) listaProdutos.getModel();
                listaProdutos.setRowSorter(new TableRowSorter(model));
                model.setNumRows(0);

                for (int i = 0; i < listagem.size(); i++) {
                    model.addRow(new Object[]{
                        listagem.get(i).getId(),
                        listagem.get(i).getNome(),
                        listagem.get(i).getValor(),
                        listagem.get(i).getStatus()
                    });
                }

                conecta.desconectar();
            }
        } catch (Exception e) {        
            System.out.println("Erro ao listar produtos: " + e.getMessage());
        }
    }

